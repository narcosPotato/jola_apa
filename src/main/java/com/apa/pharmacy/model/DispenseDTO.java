package com.apa.pharmacy.model;

public class DispenseDTO {
	
	private String dispenseSeq;
	private String pharmacyID;
	private String hospitalName;
	private String userName;
	private String childName;
	private String userTel;
	private String treatmentDate;
	private String regdate;
	private String pickupWay;
	private String dispenseStatus;
	
	public String getDispenseSeq() {
		return dispenseSeq;
	}
	public void setDispenseSeq(String dispenseSeq) {
		this.dispenseSeq = dispenseSeq;
	}
	public String getPharmacyID() {
		return pharmacyID;
	}
	public void setPharmacyID(String pharmacyID) {
		this.pharmacyID = pharmacyID;
	}
	public String getHospitalName() {
		return hospitalName;
	}
	public void setHospitalName(String hospitalName) {
		this.hospitalName = hospitalName;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getChildName() {
		return childName;
	}
	public void setChildName(String childName) {
		this.childName = childName;
	}
	public String getUserTel() {
		return userTel;
	}
	public void setUserTel(String userTel) {
		this.userTel = userTel;
	}
	public String getTreatmentDate() {
		return treatmentDate;
	}
	public void setTreatmentDate(String treatmentDate) {
		this.treatmentDate = treatmentDate;
	}
	public String getRegdate() {
		return regdate;
	}
	public void setRegdate(String regdate) {
		this.regdate = regdate;
	}
	public String getPickupWay() {
		return pickupWay;
	}
	public void setPickupWay(String pickupWay) {
		this.pickupWay = pickupWay;
	}
	public String getDispenseStatus() {
		return dispenseStatus;
	}
	public void setDispenseStatus(String dispenseStatus) {
		this.dispenseStatus = dispenseStatus;
	}
	
	
	
	
}



